	function armarConsulta($condicion,$papelera){
		if ($condicion!=""){
			$consulta="WHERE cursos.idcurso <>'0'";
		}else{
			$consulta="WHERE cursos.idcurso <>'0'";
		}
		return $consulta;
	}function comprobarCampo($campo, $valor, $tipoGuardado){
		if($this->con->conectar()==true){
			//print_r($listaCampos);
			$resultado=mysqli_query($this->con->conect,"SELECT COUNT( * ) AS contador from cursos WHERE $campo = '$valor'");
			if ($resultado){
				$extractor = mysqli_fetch_array($resultado);
				$numero_filas=$extractor["contador"];
				if ($tipoGuardado=='nuevo'){
					if ($numero_filas=="0"){
						return false;
					}else{
						return true;
					}
				}
				if ($tipoGuardado=='modificar'){
					if ($numero_filas=="1" or $numero_filas=="0"){
						return false;
					}else{
						return true;
					}
				}
			}else{
				return false;
			}
		}
	}

	function guardar($nombre,$categoria,$icono,$ContadorLecciones, $aTipoLecciones){
		/////PERMISOS////////////////
		if (!isset($_SESSION['permisos']['cursos']['guardar'])){
			return "denegado";
			exit;
		}
		/////FIN  DE PERMISOS////////
		$idcurso=$this->con->generarClave(2); /*Sincronizacion 1 */
		/* 
			Traer variable contador y hacer un for sacando todos los campos de los arreglos
		*/
		for ($i=0; $i <= $ContadorLecciones ; $i++) { 
				$tipoLeccion = $aTipoLecciones[$i];
				// $inputLeccion ="";
				// $textareaLeccion ="";
				mysqli_query($this->con->conect,"INSERT INTO detallecurso (tipo) VALUES ('$tipoLeccion')");
		}
		// $guardarLeccion = mysqli_query($this->con->conect,"INSERT INTO detallecurso");
		if($this->con->conectar()==true){
			if($this->comprobarCampo("nombre",$nombre, "nuevo")){
				return "nombreExiste";
			}else{
				if(mysqli_query($this->con->conect,"INSERT INTO cursos (idcurso, nombre, categoria, icono) VALUES ('$idcurso','$nombre','$categoria','$icono')")){
					//BITACORA
					if ($_SESSION['bitacora']=="si"){
						$descripcionB="agreg&oacute; un nuevo registro en la tabla cursos ";
						$this->registrarBitacora("guardar",$descripcionB);
					}
					return "exito";
				}else{
					return "fracaso";
				}
			}
		}
	}
	
	function actualizar($nombre,$categoria,$icono,$idcurso){
		/////PERMISOS////////////////
		if (!isset($_SESSION['permisos']['cursos']['modificar'])){
			return "denegado";
			exit;
		}
		/////FIN  DE PERMISOS////////
		
		if($this->con->conectar()==true){
			if($this->comprobarCampo("nombre",$nombre, "modificar")){
				return "nombreExiste";
			}else{
				if(mysqli_query($this->con->conect,"UPDATE cursos SET nombre='$nombre', categoria='$categoria', icono='$icono' WHERE idcurso='$idcurso'")){
					//BITACORA
					if ($_SESSION['bitacora']=="si"){
						$descripcionB="modific&oacute; el registro ID: $idcurso, de la tabla cursos ";
						$this->registrarBitacora("modificar",$descripcionB);
					}
					return "exito";
				}else{
					return "fracaso";
				}
			}
		}
	}
	
